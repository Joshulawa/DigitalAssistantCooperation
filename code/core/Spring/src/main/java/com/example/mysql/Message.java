package com.example.mysql;

public class Message {

    private String author;
    private String content;

    public Message(String author, String content) {
        this.author = author;
        this.content = content;

    }
    public Message(){}

    public String getAuthor() {
        return author;
    }
    
    public String getContent() {
        return content;
    }









}
